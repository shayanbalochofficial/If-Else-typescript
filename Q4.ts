//? Q4 => Create a program to check a student grade and percentage.
let marks: number = 29;

//! Checking The Grade Using IF-Else

if (marks >= 90) {
    console.log("Grade: A*");
} else if (marks >= 80) {
    console.log("Grade: A");
} else if (marks >= 70) {
    console.log("Grade: B");
} else if (marks >= 60) {
    console.log("Grade: C");
} else if (marks >= 50) {
    console.log("Grade: D");
} else if (marks >= 40) {
    console.log("Grade: E");
} else {
    console.log("Grade : U\nHope You perform better Next Time.");
}
